#include <iostream>

int main()
{
	int num1 = 5;
	if (num1 == 6) {
		std::cout << "num = 6";
	}
	else if (num1 == 5)
	{
		std::cout << "num = 5";
	}
	else {
		std::cout << "num != 6";
	}
	if (num1 != 6) {
		std::cout << "num != 6";
	}
	//можно проверять на условия: == равно; !=неравно;<меньше; >больше; <=меньше или равно; >=больше или равно//

	return 0;
}